
package com.avery.networking.model.beer;

public class LabelImage {

    private String original;
    private String desktop;
    private String desktop2x;

    /**
     * @return The original
     */
    public String getOriginal() {
        return original;
    }

    /**
     * @param original The original
     */
    public void setOriginal(String original) {
        this.original = original;
    }

    /**
     * @return The desktop
     */
    public String getDesktop() {
        return desktop;
    }

    /**
     * @param desktop The desktop
     */
    public void setDesktop(String desktop) {
        this.desktop = desktop;
    }

    /**
     * @return The desktop2x
     */
    public String getDesktop2x() {
        return desktop2x;
    }

    /**
     * @param desktop2x The desktop2x
     */
    public void setDesktop2x(String desktop2x) {
        this.desktop2x = desktop2x;
    }

}
